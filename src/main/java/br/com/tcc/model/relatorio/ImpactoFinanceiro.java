package br.com.tcc.model.relatorio;

public class ImpactoFinanceiro {
	
	private double totalAbastecimento;
	private double totalManutencao;
	private double totalMulta;
	
	public double getTotalAbastecimento() {
		return totalAbastecimento;
	}
	public void setTotalAbastecimento(double totalAbastecimento) {
		this.totalAbastecimento = totalAbastecimento;
	}
	public double getTotalManutencao() {
		return totalManutencao;
	}
	public void setTotalManutencao(double totalManutencao) {
		this.totalManutencao = totalManutencao;
	}
	public double getTotalMulta() {
		return totalMulta;
	}
	public void setTotalMulta(double totalMulta) {
		this.totalMulta = totalMulta;
	}
	
}